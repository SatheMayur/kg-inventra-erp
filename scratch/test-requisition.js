const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();
const crypto = require('crypto');

async function testRequisition() {
  const userId = 'cmpc9b140000ndqxgv1dhkcxp';
  const itemId = 'cmq67vr0w000380wgu4b9gip3'; // Blue Gel Pen
  const qty = 150; // requested

  console.log('--- Starting Requisition Deficit Test ---');
  
  // Clean up any old POs or requests for this test
  await prisma.purchaseOrder.deleteMany({ where: { notes: { startsWith: 'Auto-generated: Shortage for Request' } } });
  
  // Reset item reservedQty to 0
  await prisma.item.update({ where: { id: itemId }, data: { reservedQty: 0 } });
  
  const user = await prisma.user.findUnique({ where: { id: userId } });
  const item = await prisma.item.findUnique({ where: { id: itemId } });
  
  console.log(`Original Stock: ${item.stock}, Reserved: ${item.reservedQty}`);

  const lines = [{ itemId, qty }];
  const qtyByItem = new Map();
  qtyByItem.set(itemId, qty);

  const result = await prisma.$transaction(async (tx) => {
    const lineData = [];
    const poBySupplier = new Map();
    let hasDeficit = false;

    for (const [itId, totalQty] of qtyByItem) {
      const it = await tx.item.findUnique({ where: { id: itId } });
      const available = it.stock - it.reservedQty;
      const deficit = totalQty - available;

      if (deficit > 0) {
        hasDeficit = true;
        const reserveQty = Math.max(0, available);
        if (reserveQty > 0) {
          await tx.item.update({
            where: { id: itId },
            data: { reservedQty: { increment: reserveQty }, version: { increment: 1 } },
          });
        }

        const supplierId = it.preferredSupplierId || (await tx.supplier.findFirst({ select: { id: true } }))?.id;
        if (supplierId) {
          if (!poBySupplier.has(supplierId)) {
            poBySupplier.set(supplierId, []);
          }
          poBySupplier.get(supplierId).push({
            itemId: itId,
            qty: deficit,
            unitPrice: Math.max(0, Math.round(it.price || 0)),
          });
        }
      } else {
        await tx.item.update({
          where: { id: itId },
          data: { reservedQty: { increment: totalQty }, version: { increment: 1 } },
        });
      }

      lineData.push({
        itemId: itId,
        itemName: it.name,
        requestedQty: totalQty,
        availableQtySnapshot: Math.max(0, available),
        unit: it.unit,
        status: 'Pending',
      });
    }

    const status = hasDeficit ? 'Purchase Pending' : 'Pending';

    const req = await tx.request.create({
      data: {
        userId,
        employee: user.name,
        department: user.department,
        status,
        lines: { create: lineData },
      },
      include: { lines: true },
    });

    const date = new Date().toLocaleDateString('sv-SE', { timeZone: 'Asia/Kolkata' }).replace(/-/g, '');
    for (const [supplierId, poItems] of poBySupplier) {
      const poCount = await tx.purchaseOrder.count({
        where: { poNumber: { startsWith: `PO-AUTO-${date}` } },
      });
      const randomPart = crypto.randomBytes(2).toString('hex');
      const poNumber = `PO-AUTO-${date}-${(poCount + 1).toString().padStart(2, '0')}-${randomPart}`;
      const totalAmount = poItems.reduce((sum, item) => sum + item.qty * item.unitPrice, 0);

      await tx.purchaseOrder.create({
        data: {
          poNumber,
          supplierId,
          status: 'DRAFT',
          autoGenerated: true,
          totalAmount,
          notes: `Auto-generated: Shortage for Request ${req.id}`,
          items: {
            create: poItems.map((p) => ({
              itemId: p.itemId,
              qty: p.qty,
              unitPrice: p.unitPrice,
            })),
          },
        },
      });
    }

    return req;
  });

  console.log(`Requisition created! ID: ${result.id}, Status: ${result.status}`);
  
  // Verify Item reservedQty
  const updatedItem = await prisma.item.findUnique({ where: { id: itemId } });
  console.log(`Updated Item Reserved Qty: ${updatedItem.reservedQty} (expected: 120)`);
  
  // Verify PO was created
  const createdPO = await prisma.purchaseOrder.findFirst({
    where: { notes: `Auto-generated: Shortage for Request ${result.id}` },
    include: { items: true }
  });
  console.log(`Auto PO created? ${createdPO ? 'Yes' : 'No'}`);
  if (createdPO) {
    console.log(`PO Number: ${createdPO.poNumber}, Qty ordered: ${createdPO.items[0].qty} (expected: 30)`);
  }
}

testRequisition()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
